package com.java.operadores;

public class OperadoresDeAtribuicao {

	public static void main(String[] args) {
		
		int  x = 10;
		x = x + 15;
		
		System.out.println(x);

		//agilidade
		int y = 10;
		y += 15;
		System.out.println(y);
		y -= 15;
		System.out.println(y);
		y *= 15;
		System.out.println(y);
		y /= 15;
		System.out.println(y);
		
	}

}
